base = '/home/noda/Projects/romans-spectors/data/mean/'

import pandas as pd
import numpy as np

all_data = []

for file in mean_csv_files:
    try:
        # Читаем файл
        df = pd.read_csv(base + file, sep=';')

        # Нам нужны только числовые данные для развертки (Wave, Intensity, Prominence)
        # А метаданные (Brain, Label) возьмем один раз для всей строки
        meta_brain = df['Brain'].iloc[0]
        meta_label = df['Label'].iloc[0]

        # Выбираем только значимые колонки
        numeric_cols = ['Wave', 'Intensity', 'Prominence']
        subset = df[numeric_cols]

        # Если строк больше 6, берем первые 6. Если меньше — дополняем нулями.
        if len(subset) > 6:
            subset = subset.iloc[:6]

        # Превращаем в плоский массив (18 значений: 3 колонки * 6 строк)
        flat_data = subset.values.flatten()

        # Если данных не хватает до 18 значений (меньше 6 строк), дополняем нулями
        if len(flat_data) < 18:
            flat_data = np.pad(flat_data, (0, 18 - len(flat_data)), 'constant', constant_values=0)

        # Создаем названия колонок (Wave_1, Intensity_1, Prominence_1, Wave_2...)
        column_names = []
        for i in range(1, 7):
            column_names.extend([f'Wave_{i}', f'Intensity_{i}', f'Prominence_{i}'])

        # Собираем итоговую строку для этого файла
        row = pd.Series(flat_data, index=column_names)
        row['Brain'] = meta_brain
        row['Label'] = meta_label

        all_data.append(row)
    except Exception as e:
        print(f"Ошибка в файле {file}: {e}")

# Объединяем всё в один DataFrame
result_df = pd.DataFrame(all_data)

# Переставляем колонки, чтобы метаданные были в начале
cols = ['Brain', 'Label'] + [c for c in result_df.columns if c not in ['Brain', 'Label']]
result_df = result_df[cols]

# Сохраняем результат
result_df.to_csv(base + "combined_raman_data.csv", index=False)
print("Файлы успешно объединены!")
